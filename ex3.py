# This line will print a normal string
print("I will now count my chickens:")

# Division operator will result floating point instead of integer
print("Hens", 25 + 30 / 6)
# % operator aka modulus operator result the remaining
print("Roosters", 100 - 25 * 3 % 4)

# This line will print a normal string
print("Now I will count the eggs:")

# Multiplication, division, and modulus operators are higher priority than addition and subtraction
print(3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6)

# This line will print a normal string
print("Is is true that 3 + 2 < 5 - 7?")

# This line will print either True or False depending on result of the comparison operation
print(3 + 2 < 5 - 7)

# This line will print a string plus the result of addition operation
print("What is 3 + 2?", 3 + 2)
# This line will print a string plus the result of subtraction operation
print("What is 5 - 7?", 5 - 7)

# This line will print a normal string
print("Oh, that's why it's False.")

# This line will print a normal string
print("How about some more.")
# These line will print strings plus the results from the comparison operations
print("Is it greater?", 5 > -2)
print("Is it greater or equal?", 5 >= -2)
print("Is it less or equal?", 5 <= -2)
